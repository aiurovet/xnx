class Ascii {
  static const asterisk = 0x2A; // *
  static const at = 0x40; // @
  static const backSlash = 0x5C; // \
  static const braceOpen = 0x7B; // {
  static const braceShut = 0x7D; // }
  static const colon = 0x3A; // :
  static const digitZero = 0x30; // 0
  static const digitNine = 0x39; // 9
  static const dollar = 0x24; // $
  static const equality = 0x3D; // =
  static const hash = 0x23; // #
  static const lowerA = 0x61; // a
  static const lowerZ = 0x7A; // z
  static const minus = 0x2D; // -
  static const parenthesisOpen = 0x28; // (
  static const parenthesisShut = 0x29; // )
  static const tilde = 0x7E; // ~
  static const underscore = 0x5F; // _
  static const upperA = 0x41; // A
  static const upperZ = 0x5A; // Z
}